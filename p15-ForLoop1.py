# for loop as a sequence and collection

name = ['Ankita', 'Sakshi', 'Sagar', 'Lopi']
n = (23, 45, 34, 78)
per = [56.3, 89.23, 90.98, 78.60]
student = ['Ankita', 23, 56.3]
for a in name:
    print(a, end=' ')
print()
for b in n:
    print(b)
print()
for p in per:
    print(p, end=' ')
print()
for s in student:
    print(s, end=' ')
    