# +,-,*,/,%,^,floor division
a = 5
b = 2
add = a + b

print("Addition =", add)

sub = b - a
print("Subtraction =", sub)

mul = a * b
print("Multiplication =", mul)

div = b / a
print("Division =", div)

mod = a / b
print("Module =", mod)

expo = a ** b
print("Exponential =", expo)

flr_div = a // b
print("Floor Division =", flr_div)

